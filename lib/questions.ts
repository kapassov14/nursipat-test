// Генерация весов для вопроса по индексу: разнообразие по типам
function buildWeightsForQuestion(index: number): { psychotypeWeights: WeightsMap; archetypeWeights: WeightsMap } {
  const psychotypeWeights: WeightsMap = {}
  const archetypeWeights: WeightsMap = {}

  const typesP = PSYCHOTYPES as unknown as string[]
  const typesA = ARCHETYPES as unknown as string[]

  // Каждый вопрос слегка смещён в сторону одного-двух типов (по индексу)
  const primaryP = index % typesP.length
  const primaryA = index % typesA.length
  const secondaryP = (index + 3) % typesP.length
  const secondaryA = (index + 2) % typesA.length

  typesP.forEach((t, i) => {
    if (i === primaryP) psychotypeWeights[t] = [-2, 0, 2]
    else if (i === secondaryP) psychotypeWeights[t] = [-1, 0, 1]
    else psychotypeWeights[t] = [0, 0, 0]
  })
  typesA.forEach((t, i) => {
    if (i === primaryA) archetypeWeights[t] = [-2, 0, 2]
    else if (i === secondaryA) archetypeWeights[t] = [-1, 0, 1]
    else archetypeWeights[t] = [0, 0, 0]
  })

  return { psychotypeWeights, archetypeWeights }
}

// Итоговый массив из 160 вопросов с весами
export const QUESTIONS: QuestionItem[] = QUESTION_TEXTS.map((text, index) => {
  const { psychotypeWeights, archetypeWeights } = buildWeightsForQuestion(index)
  return {
    id: index + 1,
    text,
    psychotypeWeights,
    archetypeWeights,
  }
})

